    C = np.zeros((len(subs), len(subs)), dtype=complex)
    for i, I in enumerate(subs):
        for j, J in enumerate(subs):
            C[i, j] = np.linalg.det(t[np.ix_(I, J)]) if m else 1.0
    return C


