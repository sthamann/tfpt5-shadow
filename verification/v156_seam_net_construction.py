import sympy as sp

from tfpt_constants import check, summary, reset, g_car, N_fam

Q, X, Y, K = sp.symbols('q x y K', real=True)


def run():
    reset()
    print("v156 seam-net construction (Route 3, exact verifiable endpoint)")

    # 1. DtN = |k|
    u = sp.exp(sp.I * K * X) * sp.exp(-sp.Abs(K) * Y)
    harmonic = sp.simplify(sp.diff(u, X, 2) + sp.diff(u, Y, 2)) == 0
    dtn = sp.simplify(-sp.diff(u, Y).subs(Y, 0) / u.subs(Y, 0))
