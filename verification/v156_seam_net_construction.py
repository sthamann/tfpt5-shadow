    P = sp.Symbol('p')
    ns = sp.expand(sp.series((th3 ** 8 + th4 ** 8) / 2, P, 0, 4 * N).removeO())
    ns_q = [ns.coeff(P, 2 * i) for i in range(N)]          # even powers = q^i
    e4_q = [E4.coeff(Q, i) for i in range(N)]
