"""FO.01 transduction invisibility -- a THEORY CONTRACT (never a scorecard row).

The S15 typing rule (SIGNATURES.md 0b.1) states: every observable is O = B.T.A;
if the translation B annihilates the non-trivial transfer/mu4 characters
(B.P_r = 0), a null on the observable is PREDICTED, not informative about the
core. The FO.01 ontology reads the 20+ FRB intensity/timing nulls exactly this
way: a coherent, threshold-triggered amplifier is a trivial-character readout.

This contract machine-checks the claim on the minimal model:

  seam channel: 3 character classes, populations p(n+1) = T p(n), with T a
  doubly-stochastic 3x3 matrix of spectrum {1, (2/3)^6, (1/3)^6} (the frozen
  transfer kernel); the scrambled control T' has the same stationary state but
  a non-kernel spectrum.

  (1) EXACT invisibility of the uniform functional: 1^T (T^n p - pi) = 0 for
      all n (the trivial character is a left eigenvector of ANY stochastic T);
      machine-checked to 1e-14.
  (2) STATISTICAL invisibility of the threshold-intensity readout: burst trains
      generated by I(n) = (total excitation)^2 * |coherent phase noise|^2 with a
      threshold trigger are statistically indistinguishable between the kernel
      seam and the scrambled seam (two-sample KS on burst-intensity ratios and
      on waiting sequences) -- the FRB null landscape as a PREDICTION.
  (3) VISIBILITY of a character-resolved readout: the observable u2^T p(n)
      (u2 = second left eigenvector) recovers the eigenvalue 64/729 to 1e-10 --
      the same channel is perfectly visible once B.P_r != 0.
  (4) VISIBILITY through an unequal-weight intensity coupling: B = (b1,b2,b3)
      with unequal weights makes the SAME burst-train statistics distinguishable
      (KS D large) -> invisibility is a property of B, not of the channel.

Pure simulation/mathematics: belongs in theory-contracts; the formal version
("for which standard emission processes is B.P_r = 0 provable?") stays an open
contract candidate.
"""
from __future__ import annotations

import json
from pathlib import Path

import numpy as np
from scipy import stats

RESULTS = Path(__file__).resolve().parent / "fo01_transduction_results.json"

LAM2 = (2.0 / 3.0) ** 6          # 64/729
LAM3 = (1.0 / 3.0) ** 6          # 1/729
N_RUNS = 8000                    # burst-train realisations per seam
N_STEPS = 8                      # transfer steps per realisation (transient-dominated)
THRESH = 1.00                    # emission threshold on the intensity proxy (vs train median)
SEED = 0


def transfer_matrix(lam2: float, lam3: float) -> np.ndarray:
    """Doubly stochastic 3x3 with spectrum {1, lam2, lam3} (orthogonal basis)."""
    q1 = np.ones(3) / np.sqrt(3.0)
    q2 = np.array([1.0, -1.0, 0.0]) / np.sqrt(2.0)
    q3 = np.array([1.0, 1.0, -2.0]) / np.sqrt(6.0)
    Q = np.stack([q1, q2, q3], axis=1)
    T = Q @ np.diag([1.0, lam2, lam3]) @ Q.T
    if T.min() < -1e-12:
        raise ValueError("transfer matrix not non-negative")
    return np.clip(T, 0.0, None)


def first_step_ratio(rng: np.random.Generator, T: np.ndarray, B: np.ndarray,
                     n_runs: int, speck: float = 0.3) -> np.ndarray:
    """I(1)/I(0) of the coherent intensity readout across random excitations.

    The transient-sensitive functional: one transfer step after a random
    character excitation (min population kept >= 0.02). With a uniform B the
    distribution must be blind to the seam spectrum; with unequal weights the
    SAME functional becomes spectrum-sensitive.
    """
    pi_ = np.ones(3) / 3.0
    out = np.empty(n_runs)
    for i in range(n_runs):
        exc = rng.standard_normal(3)
        exc -= exc.mean()
        s = (pi_.min() - 0.02) / max(-exc.min(), 1e-9)
        p0 = pi_ + exc * min(s, 0.45 / np.linalg.norm(exc))
        p1 = T @ p0
        sp0 = abs(1 + speck * (rng.standard_normal() + 1j * rng.standard_normal())) ** 2
        sp1 = abs(1 + speck * (rng.standard_normal() + 1j * rng.standard_normal())) ** 2
        r0 = float(B @ p0) / float(B @ pi_)
        r1 = float(B @ p1) / float(B @ pi_)
        out[i] = (r1 * r1 * sp1) / (r0 * r0 * sp0)
    return out


def burst_train(rng: np.random.Generator, T: np.ndarray, B: np.ndarray,
                n_steps: int) -> np.ndarray:
    """Intensities of threshold-triggered coherent emission along one recovery.

    p(0) = stationary + random character excitation; readout r(n) = B.p(n);
    coherent intensity I(n) = r(n)^2 * |1 + 0.3 xi|^2 (speckle phase noise);
    a burst is emitted where I(n) >= THRESH * median(I).
    """
    pi_ = np.ones(3) / 3.0
    exc = rng.standard_normal(3) * 0.25
    exc -= exc.mean()                      # keep total population fixed
    p = pi_ + exc
    out = np.empty(n_steps)
    for n in range(n_steps):
        r = float(B @ p) / float(B @ pi_)
        speck = abs(1.0 + 0.3 * (rng.standard_normal() + 1j * rng.standard_normal())) ** 2
        out[n] = r * r * speck
        p = T @ p
    med = np.median(out)
    return out[out >= THRESH * med]


def train_ratio_stats(rng: np.random.Generator, T: np.ndarray, B: np.ndarray
                      ) -> tuple[np.ndarray, np.ndarray]:
    """(consecutive burst-intensity ratios, per-train burst counts)."""
    ratios, counts = [], []
    for _ in range(N_RUNS):
        tr = burst_train(rng, T, B, N_STEPS)
        counts.append(len(tr))
        if len(tr) >= 2:
            ratios.append(tr[1:] / tr[:-1])
    return np.concatenate(ratios), np.array(counts, dtype=float)


def main() -> None:
    rng = np.random.default_rng(SEED)
    checks: list[tuple[str, bool, str]] = []

    T = transfer_matrix(LAM2, LAM3)
    T_scr = transfer_matrix(0.55, 0.30)         # same stationary, non-kernel spectrum

    # (1) exact invisibility of the uniform functional
    p0 = np.array([0.6, 0.3, 0.1])
    dev = max(abs(np.ones(3) @ (np.linalg.matrix_power(T, n) @ p0) - 1.0)
              for n in range(1, 40))
    checks.append(("C1 uniform functional exactly blind (1e-14)", dev < 1e-13,
                   f"max |1.T^n.p - 1| = {dev:.2e}"))

    # (2) statistical invisibility of the uniform-B intensity readout
    B_uni = np.ones(3)
    fs_ker = first_step_ratio(rng, T, B_uni, 20000)
    fs_scr = first_step_ratio(rng, T_scr, B_uni, 20000)
    ks_fs = stats.ks_2samp(fs_ker, fs_scr)
    checks.append(("C2a first-step intensity ratio blind for uniform B (KS p > 0.01, D < 0.03)",
                   ks_fs.pvalue > 0.01 and ks_fs.statistic < 0.03,
                   f"D = {ks_fs.statistic:.4f}, p = {ks_fs.pvalue:.3f}, n = 20000/20000"))
    r_ker, c_ker = train_ratio_stats(rng, T, B_uni)
    r_scr, c_scr = train_ratio_stats(rng, T_scr, B_uni)
    ks_r = stats.ks_2samp(r_ker, r_scr)
    ks_c = stats.ks_2samp(c_ker, c_scr)
    checks.append(("C2b threshold burst trains indistinguishable (KS p > 0.01, D < 0.03)",
                   ks_r.pvalue > 0.01 and ks_r.statistic < 0.03
                   and ks_c.pvalue > 0.01 and ks_c.statistic < 0.03,
                   f"ratios D = {ks_r.statistic:.4f} (p = {ks_r.pvalue:.3f}); "
                   f"counts D = {ks_c.statistic:.4f} (p = {ks_c.pvalue:.3f})"))

    # (3) character-resolved readout recovers the eigenvalue exactly
    # (evolve the DEVIATION d = p - pi directly: no cancellation loss)
    u2 = np.array([1.0, -1.0, 0.0]) / np.sqrt(2.0)
    d = np.array([0.2, -0.15, -0.05])
    o = []
    for _ in range(12):
        o.append(float(u2 @ d))
        d = T @ d
    o = np.array(o)
    lam_hat = np.mean(o[1:] / o[:-1])
    checks.append(("C3 character readout recovers 64/729 (1e-10)",
                   abs(lam_hat - LAM2) < 1e-10,
                   f"lambda_hat = {lam_hat:.12f} vs {LAM2:.12f}"))

    # (4) unequal-weight coupling makes the SAME functional visible
    B_neq = np.array([1.0, 0.4, 0.1])
    fs_ker2 = first_step_ratio(rng, T, B_neq, 20000)
    fs_scr2 = first_step_ratio(rng, T_scr, B_neq, 20000)
    ks2 = stats.ks_2samp(fs_ker2, fs_scr2)
    checks.append(("C4 unequal-weight coupling distinguishes (KS D > 0.04, p < 1e-10)",
                   ks2.statistic > 0.04 and ks2.pvalue < 1e-10,
                   f"D = {ks2.statistic:.4f}, p = {ks2.pvalue:.2e} "
                   f"vs blind D = {ks_fs.statistic:.4f}"))

    n_pass = sum(ok for _, ok, _ in checks)
    for name, ok, detail in checks:
        print(("PASS" if ok else "FAIL"), name, "--", detail)
    print(f"{n_pass}/{len(checks)} PASS")

    RESULTS.write_text(json.dumps({
        "contract": "FO.01_transduction_invisibility",
        "spectrum": [1.0, LAM2, LAM3],
        "scrambled_spectrum": [1.0, 0.55, 0.30],
        "n_runs": N_RUNS,
        "checks": [{"name": n, "pass": bool(ok), "detail": d} for n, ok, d in checks],
        "all_pass": bool(n_pass == len(checks)),
    }, indent=2))
    print(f"wrote {RESULTS}")
    if n_pass != len(checks):
        raise SystemExit(1)


if __name__ == "__main__":
    main()
